# inch 단위를 입력받아 cm 단위 구하는 코드 작성하기
inch=float(input("cm로 바꿀 인치를 입력하시오 : "))
print(inch*2.54,"(cm)")
# 킬로그램 단위 입력 받아 파운드 단위 구하는 코드 작성하기
lb=float(input("파운드 단위로 바꿀 무게를 입력하시오.(kg단위) : "))
print(lb*2.204623,'(lb)')
# 원의 반지름 입력 받아 원의 둘레와 넓이를 구하는 코드 작성하기
aaa=float(input("반지름을 입력하시오:"))
print("원의 둘레:",aaa*2*3.14)
print("원의 넓이:",3.14*aaa*aaa)
